package www.gift_vouchers.marasel.MainScreen.ui.Product.Model;

public class ImgList {
    String id, img;

    public ImgList(String id, String img) {
        this.id = id;
        this.img = img;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
}
